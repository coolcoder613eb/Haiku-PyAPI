py::str ToUTF8(uint32_t c) {
    char buffer[5] = {0};  // Un carattere UTF-8 pu√≤ essere lungo fino a 4 byte + terminatore null
    char* out = buffer;
    BUnicodeChar::ToUTF8(c, &out);
    return py::str(buffer);
}

uint32_t FromUTF8(py::buffer in) {
    py::buffer_info info = in.request();
    if (info.ndim != 1 || info.itemsize != 1) {
        throw std::runtime_error("Buffer must be a 1-dimensional array of bytes");
    }

    const char* data = static_cast<const char*>(info.ptr);
    uint32_t result = BUnicodeChar::FromUTF8(&data);
    
    return result;
}

